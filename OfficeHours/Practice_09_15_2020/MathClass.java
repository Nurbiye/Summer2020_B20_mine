        //10*10*10
       double r1 =  Math.pow(10,3);

        System.out.println(Math.pow(2,5)); //2*2*2*2*2

        //absolute value |-1|==>1
        System.out.println(Math.abs(-1));

        //square root 4==>2
        System.out.println(Math.sqrt(10));
