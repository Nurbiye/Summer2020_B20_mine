package day06_ComparisionOperators;

public class Swap1 {
    public static void main(String[] args) {
        int a =10;
        int b =15;

        int c = b; //c=15
         b=a; //b=10 b will be the value of a, swap a's value to b.
        a=c; //a=15   //swap the value (almaxturmaq)

        System.out.println(a);
        System.out.println(b);




    }



}
